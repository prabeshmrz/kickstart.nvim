local ascii = strawhat

return {
  'nvimdev/dashboard-nvim',
  event = 'VimEnter',
  opts = function()
    local logo = {}
    for _, item in ipairs(ascii) do
      table.insert(logo, item)
    end

    local opts = {
      theme = 'doom',
      hide = { statusline = true },
      config = {
        header = logo,
        center = {
          { icon = ' ', desc = 'Find File   ', key = 'f', key_format = ' %s', action = 'Telescope find_files' },
          { icon = ' ', desc = 'New File    ', key = 'n', key_format = ' %s', action = 'enew' },
          { icon = ' ', desc = 'Projects    ', key = 'p', key_format = ' %s', action = 'Telescope projects' },
          { icon = ' ', desc = 'Recent Files', key = 'r', key_format = ' %s', action = 'Telescope oldfiles' },
          { icon = ' ', desc = 'Find Text   ', key = 't', key_format = ' %s', action = 'Telescope live_grep' },
          { icon = ' ', desc = 'Configuration                               ', key = 'c', key_format = ' %s', action = 'e ~/.config/nvim/init.lua' },
          { icon = ' ', desc = 'Quit        ', key = 'q', key_format = ' %s', action = 'qa' },
        },
      },
    }
    for _, button in ipairs(opts.config.center) do
      button.desc = button.desc .. string.rep(' ', 16 - #button.desc)
    end

    return opts
  end,
}
